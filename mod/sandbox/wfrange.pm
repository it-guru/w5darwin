use strict;
use vars qw(@ISA);
use kernel;
use kernel::App::Web;
use kernel::DataObj::DB;
use kernel::Field;
@ISA=qw(kernel::App::Web::Listedit kernel::DataObj::DB);

sub new
{
   my $type=shift;
   my %param=@_;
   my $self=bless($type->SUPER::new(%param),$type);

   $self->AddFields(
      new kernel::Field::Linenumber(
                name          =>'linenumber',
                label         =>'No.'),

      new kernel::Field::Id(
                name          =>'id',
                uivisible     =>0,
                sqlorder      =>'desc',
                label         =>'W5BaseID',
                dataobjattr   =>'wfrange.id'),
                                                  
      new kernel::Field::Link(
                name          =>'wfheadid',
                uivisible     =>0,
                sqlorder      =>'desc',
                label         =>'WfHeadID',
                dataobjattr   =>'wfrange.wfheadid'),

      new kernel::Field::Vector(
                name          =>'range',
                uivisible     =>0,
                sqlorder      =>'desc',
                label         =>'Timerange',
                dataobjattr   =>'wfrange.range'),
                                                  
   );
   $self->setDefaultView(qw(id));
   $self->setWorktable("wfrange");
   return($self);
}

sub Validate
{
   my $self=shift;
   my $oldrec=shift;
   my $newrec=shift;

   return(1);
}


sub isViewValid
{
   my $self=shift;
   my $rec=shift;
   return("header","default") if (!defined($rec) && $self->IsMemberOf("admin"));
   return("ALL");
}

sub isWriteValid
{
   my $self=shift;
   my $rec=shift;
   return("default") if ($self->IsMemberOf("admin"));
   return(undef);
}





1;
