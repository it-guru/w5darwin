                name          =>'producer',
                label         =>'Producer',
                vjoineditbase =>{'cistatusid'=>"<5"},
                vjointo       =>'itil::producer',
                vjoinon       =>['producerid'=>'id'],
                vjoindisp     =>'name'),

      new kernel::Field::Link(
                name          =>'producerid',
