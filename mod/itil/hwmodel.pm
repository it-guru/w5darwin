      new kernel::Field::Id(
                name          =>'id',
                sqlorder      =>'desc',
                label         =>'W5BaseID',
                dataobjattr   =>'hwmodel.id'),
                                                  
      new kernel::Field::Text(
                name          =>'name',
                htmlwidth     =>'180px',
                label         =>'Name',
                dataobjattr   =>'hwmodel.name'),

      new kernel::Field::RecordUrl(),

      new kernel::Field::Select(
                name          =>'cistatus',
                htmleditwidth =>'40%',
